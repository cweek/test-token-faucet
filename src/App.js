const contractAddress = '0x5e139a17d501F278e7B5C1913d5841cDa9F8788e';

const TokenFaucetRequest = () => {
  const [userAddress, setUserAddress] = useState('');
  const [message, setMessage] = useState('');

  const requestTokens = async () => {
    if (!window.ethereum) {
      setMessage('MetaMask is not installed!');
      return;
    }

    try {
      // Request access the user's MetaMask account
      await window.ethereum.request({ method: 'eth_requestAccounts' });

      const provider = new ethers.providers.Web3Provider(window.ethereum);
      const signer = provider.getSigner();
      
      // Create instance of smart contract
      const tokenFaucet = new ethers.Contract(contractAddress, tokenFaucetABI, signer);
      
      // Call requestTokens function from smart contract
      const tx = await tokenFaucet.requestTokens();
      setMessage('Transaction submitted! Waiting for confirmation...');
      
      // Wait for transaction to be confirmed
      await tx.wait();
      setMessage('Tokens successfully requested!');
    } catch (error) {
      console.error('Error requesting tokens:', error);
      setMessage(`Error: ${error.reason}`);
    }
  };

  return (
    <div className="container">
      <h1>Chewy Test Tokens Faucet</h1>
      <button onClick={requestTokens}>Request Tokens</button>
      {message && <p>{message}</p>}
    </div>
  );
};

export default TokenFaucetRequest;
